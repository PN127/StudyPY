num1 = int(input('Веведите первое число: '))
num2 = int(input('Веведите второе число: '))
op = input('Введите математическое действие: ')

if op == '+':
    print ('Result: ', num1+num2)
if op == '-':
    print ('Result: ', num1-num2)
if op == '*':
    print ('Result: ', num1*num2)
if op == '/':
    print ('Result: ', num1//num2)

